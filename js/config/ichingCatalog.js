  function expectedLineName(entry, index) {
    const yinYang = entry.lineKey[index] === "1" ? "九" : "六";
    if (index === 0) return "初" + yinYang;
    if (index === 5) return "上" + yinYang;
    return yinYang + ["", "二", "三", "四", "五"][index];
  }

  function deriveReceivedLineSemantics(entry, stage, text, interpretation) {
    const verdicts = extractVerdicts(text);
    const normalized = String(text || "").replace(/貞/g, "贞").replace(/厲/g, "厉").replace(/無/g, "无");
    const tags = [entry.keywords[0], stage.keyword];
    const favorable = [];
    const unfavorable = [];
    const extraConditions = [];
    function add(list, value) { if (value && list.indexOf(value) === -1) list.push(value); }
    function hasVerdict(values) { return verdicts.some(function(item) { return values.indexOf(item) !== -1; }); }
    const clauses = String(interpretation || "").split(/[，；。]/).map(function(item) { return item.trim(); }).filter(Boolean);

    if (/孚/.test(normalized)) add(tags, "诚信");
    if (/往|征|行/.test(normalized)) add(tags, "行动");
    if (/婚|女|妇|夫|妹/.test(normalized)) add(tags, "关系");
    if (/王|君|大人|侯/.test(normalized)) add(tags, "责任");
    if (/祭|祀|禴|亨祀/.test(normalized)) add(tags, "礼仪");
    if (/凶|厉|灾|眚|血|寇/.test(normalized)) add(tags, "风险");
    if (/悔|吝/.test(normalized)) add(tags, "调整");
    clauses.forEach(function(clause) {
      const adverseClause = /不可|不宜|勿|莫|避免|贸然|冒进|强行|硬撑|逞强|贪|纵|拒绝|忽视|拖延|失信|谄媚|则凶|会凶|而凶|危险|受困|损失|失败/.test(clause) &&
        !/无不利|无咎|不必|不至/.test(clause);
      if (!adverseClause && /等待|停止|止损|退|返回|收束|守正|谨慎|警惕|诚信|求助|合作|包容|节制|修复|改变|纠正|准备|防范|安守|自守|反省|承担|整治|开放|共享|有利|可吉|则吉/.test(clause)) {
        add(favorable, clause.replace(/^(若|如|只有|必须|须|应当|应|宜)/, ""));
      }
      if (adverseClause) {
        add(unfavorable, clause.replace(/^(若|如)/, ""));
      }
      if (/才|方可|前提|只有|必须|须|若|如果|守正|条件|时机/.test(clause)) {
        add(extraConditions, clause + "。");
      }
    });

    if (hasVerdict(["supremeGood", "greatGood", "good", "correctGood", "nothingUnfavorable", "favorable"])) {
      add(favorable, "守正并按辞文所示条件行动");
    }
    if (hasVerdict(["noBlame"])) add(favorable, "谨慎处理并及时纠偏以求无咎");
    if (hasVerdict(["regretGone"])) add(favorable, "把握调整时机以消除悔意");
    if (hasVerdict(["danger", "difficulty", "regret", "unfavorable", "bad", "advanceBad", "mustNot", "doNotAct"])) {
      add(favorable, "先辨明风险并保留退路");
    }
    if (/利[见見]大人/.test(normalized)) add(favorable, "寻求可靠且有经验者协助");
    if (/利涉大川/.test(normalized)) add(favorable, "条件齐备后承担必要行动");
    if (favorable.length === 0) add(favorable, "结合爻位与现实条件审势");

    if (hasVerdict(["bad", "advanceBad", "mustNot", "unfavorable"])) add(unfavorable, "强行推进辞文明示不利之事");
    if (hasVerdict(["danger", "difficulty", "regret"])) add(unfavorable, "忽视危险、困难或悔吝信号");
    if (hasVerdict(["doNotAct"])) add(unfavorable, "在时机未明时贸然启用力量");
    if (unfavorable.length === 0) add(unfavorable, "脱离辞文条件作武断行动");

    if (/贞/.test(normalized) && !hasVerdict(["correctGood"])) add(extraConditions, "以守正和持续合宜为前提。");
    if (/小.+吉.+大.+凶/.test(normalized)) add(extraConditions, "小事可守正，大事不可照搬同一做法。");
    if (/終吉|终吉/.test(normalized)) add(extraConditions, "吉在最终结果，过程仍需守持相应条件。");

    return {
      situationTags: tags,
      favorableActions: favorable,
      unfavorableActions: unfavorable,
      conditions: extraConditions
    };
  }

  function buildTopicDirections(entry) {
    const theme = entry.imageMeaning.replace(/[。；]$/, "");
    return Object.keys(topicFrames).reduce(function(result, topicId) {
      const frame = topicFrames[topicId];
      result[topicId] = frame.prefix + "：" + theme + "。重点不是预测结果，而是据此调整当下可控的选择。";
      return result;
    }, {});
  }

  function buildLineEntries(entry) {
    const reviewed = qianKunLines[entry.id];
    const received = receivedLineTexts[entry.id];
    const interpretations = receivedLineInterpretations[entry.id];
    return lineStages.map(function(stage, index) {
      const data = reviewed && reviewed[index];
      const expectedName = expectedLineName(entry, index);
      let meaning;
      if (data) {
        meaning = makeTextMeaning(data[0], data[1], data[2], { favorable: data[3], unfavorable: data[4] }, "reviewed");
        meaning.needsSemanticReview = false;
      } else {
        const sourceLine = received && received[index];
        const interpretation = interpretations && interpretations[index];
        if (!sourceLine || sourceLine[0] !== expectedName || !sourceLine[1] || !interpretation) {
          throw new Error("《周易》爻辞数据不完整或爻名不符：" + entry.id + " " + entry.name + " " + expectedName);
        }
        const reviewKey = entry.id + ":" + (index + 1);
        const reviewReason = semanticReviewNotes[reviewKey] || "";
        const semantics = deriveReceivedLineSemantics(entry, stage, sourceLine[1], interpretation);
        meaning = makeTextMeaning(
          sourceLine[1],
          interpretation,
          semantics.situationTags,
          { favorable: semantics.favorableActions, unfavorable: semantics.unfavorableActions },
          reviewReason ? "reviewedWithInterpretiveVariant" : "reviewed"
        );
        semantics.conditions.forEach(function(condition) {
          if (meaning.conditions.indexOf(condition) === -1) meaning.conditions.push(condition);
        });
        meaning.needsSemanticReview = Boolean(reviewReason);
        if (reviewReason) meaning.semanticReviewReason = reviewReason;
      }
      meaning.position = index + 1;
      meaning.label = expectedName;
      meaning.lineName = expectedName;
      meaning.keyword = entry.keywords[index % entry.keywords.length] + "·" + stage.keyword;
      return meaning;
    });
  }

